while path_length > 0:
    # 走橫向的邊
    for _ in range(path_length):
        move()
        harvest()
    turn_left()
    path_length -= 1 # 邊長減一
    
    if path_length == 0: # 如果走完了就跳出
        break
        
    # 走縱向的邊
